CREATE TABLE JOB_HISTORY (EMPLOYEE_ID DECIMAL(6) NOT NULL, START_DATE DATE NOT NULL, END_DATE DATE NOT NULL, JOB_ID VARCHAR(10) NOT NULL, DEPARTMENT_ID DECIMAL(4));

CREATE TABLE LOCATIONS(LOCATION_ID DECIMAL(4) NOT NULL, STREET_ADDRESS VARCHAR(40), POSTAL_CODE VARCHAR(12), CITY VARCHAR(30) NOT NULL, STATE_PROVINCE VARCHAR(25), COUNTRY_ID CHAR(2));

CREATE TABLE REGIONS (REGION_ID DECIMAL NOT NULL, REGION_NAME VARCHAR(25));



CREATE TABLE COUNTRIES (COUNTRY_ID CHAR(2) NOT NULL, COUNTRY_NAME VARCHAR(40), REGION_ID DECIMAL);

CREATE TABLE DEPARTMENTS (DEPARTMENT_ID DECIMAL(4) NOT NULL, DEPARTMENT_NAME VARCHAR(30) NOT NULL, MANAGER_ID DECIMAL(6), LOCATION_ID DECIMAL(4));

